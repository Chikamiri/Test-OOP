package com.mycompany.app.Week1_Code2;

public class While_For_Loop {
    public void khanh5(){
        int i=1;
        do{
            for(int j=0; j<i; j++){
                System.out.print("*");
            }
            System.out.println(" ");
            i++;
        }
        while(i<=5);
    } 
}
